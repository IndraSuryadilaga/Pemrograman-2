putaran = 5
jarak = 14

# Rumus untuk menghitung jari-jari
jarijari = jarak / (2 * 3.14159 * putaran)

print(f'Diketahui:\nPak dengklek mengelilingi taman = {putaran} Putaran')
print(f'Jarak tempuh Pak dengklek = {jarak} Kilometer\n')
print(f'Jawab :\nJari-jari taman yang dikelilingi Pak Dengklek adalah {jarijari:.2f} Kilometer')
